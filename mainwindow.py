    # This method checks whether the input entered by the user is violating the rules of Sudoku or not
    def checkInputFeasibility(self, arr):
        for i in range(maxBlock):
            for j in range(maxBlock):
                if arr[i][j] != 0:
                    flag = isInputFeasible(arr, i, j, arr[i][j])

                    if not flag:
                        return False
        
        return True

    # This method deals with the fundamental control of the program flow.
    # This method is called and triggered every time the 'Solve' PushButton is pressed
    def controler(self):

        arr = self.initialize()
        flag = self.checkInputFeasibility(arr)

        if flag:
            self.label_35.setHidden(True)
            self.label_35.setDisabled(True)
            place(arr, 0, 0)
            self.showOutput(arr)
        else:
            self.label_35.setStyleSheet('color: red')
            self.label_35.setEnabled(True)
            self.label_35.setHidden(False)

